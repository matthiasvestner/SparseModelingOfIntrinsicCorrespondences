%////////////////////////////////////////////////////////////////////////////////////////

%%
%   O: Matrix O
%   mu: mu
%   alpha: alpha
%
%   P2(O)
function P2=generateP2(O, mu, alpha)

%////////////////////////////////////////////////////////////////////////////////////////

P2 = zeros(size(O));

for i=1:size(O)(1),
    n = norm(O(i,:));
    P2(i,:) = max((n-mu/alpha), 0) * O(i,:) / n;
end

end % generateP2
